public class activity1{
   
    // Addition
   
    public static double add(double num1, double num2){
        return num1 + num2;
    }

    // Subtraction
   
    public static double subtract(double num1, double num2){
        return num1 - num2;
    }

    // Division

    public static double divide(double num1, double num2){
        return num1 / num2;
    }
}